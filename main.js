//variable representing my age
const myAge = 29;

//setting a variable for the amount of early years
let earlyYears = 2;

earlyYears = earlyYears * 10.5

//subtracting two because we've accounted for the first two years
let laterYears = myAge - 2;

//no. of dog years accounted for in later years
laterYears *= 4;

//Adding all the years to get my age in dog years
myAgeInDogYears = earlyYears + laterYears;

//creating a string and converting it to lowercase
const myName = 'Ben' .toLowerCase();

//logging to the console my work
console.log(`My name is ${myName}. I am ${myAge} years old
in human years which is ${myAgeInDogYears} years old in dog 
years.`);
 
